cars = 3
people = 8

people_per_car = people / cars

# Round to 1 decimal
print(f'The are {people_per_car:.1f} people in each car.')

# Round to 2 decimal
print(f'The are {people_per_car:.2f} people in each car.')

# Round to 3 decimal
print(f'The are {people_per_car:.3f} people in each car.')

